<p>
<strong>AirJasmin</strong> sudah menerapkan cara yang lebih nyaman untuk memesan tiket, dengan 5 (lima) alternatif metode pembayaran on-line; ATM, Phone Banking, Internet Banking, Mobile Banking, atau menggunakan kartu kredit Visa / MasterCard. Selain itu anda juga masih bisa menggunakan cara lama membeli tiket dari Travel Agent atau langsung dari Kantor Cabang AirJasmin .<br />
<br />
<u><br />
<strong>Online System Reservations :</strong></u>
</p>
<p>
<strong>Step 1</strong>. Panggil AirJasmin Call Center 24 jam dan membuat pesanan. Anda akan menerima nomor Referensi Nama Penumpang (PNR) dan 13 digit kode pembayaran.<br />
<br />
<strong>Step 2. </strong>Untuk membayar tiket Anda dapat menggunakan salah satu cara berikut:
</p>
<ul id="kiri">
	<li>Dari mesin ATM Bank Central Asia (BCA), Bank Negara Indonesia (BNI), Bank Mandiri, Bank Niaga, Lippo Bank, Bank Permata. Bank BRI. Bank BRI.</li>
	<li>Phone Banking dari Bank Mandiri, BNI, BCA</li>
	<li>Internet Banking dari Bank Mandiri, BCA</li>
	<li>Mobile Banking dari Bank Mandiri, BCA</li>
	<li>Memakai Visa atau MasterCard kartu kredit</li>
</ul>
<p>
Jika pembayaran melalui mesin ATM, Anda akan menerima slip kertas sebagai konfirmasi pembayaran. Jika pembayaran melalui Phone Banking, Internet Banking, atau Mobile Banking, tidak ada kertas catatan yang diperlukan. Bila pembayaran melalui kartu kredit, Anda dapat hubungi AirJasmin Call Center 24 jam dan kunci dalam 16 digit nomor kartu kredit untuk mengotorisasi pembayaran.
</p>
<p>
Dengan AirJasmin e-tiket pada semua penerbangan domestik dan penerbangan ke Singapura yang berasal di Jakarta, kertas tradisional tiket tidak lagi diperlukan.<br />
<br />
<strong>Step 3.</strong> Menuju ke bandara, menunjukkan paspor atau kartu identitas di <em>check in</em> dan melakukan penerbangan Anda.
</p>
